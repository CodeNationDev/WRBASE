import Foundation
import UIKit

extension UIFont {
    
    public class func openSansRegular(size: CGFloat) -> UIFont {
        UIFont(name: "OpenSans-Regular", size: size)!
    }
    public class func openSansSemiBold(size: CGFloat) -> UIFont {
        UIFont(name: "OpenSans-SemiBold", size: size)!
    }
    
}
